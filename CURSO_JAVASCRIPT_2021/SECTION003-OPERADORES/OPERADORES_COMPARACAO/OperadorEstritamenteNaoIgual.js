var var1 = 3;
var var2 = 4;

/**
 * * OPERADOR: Estritamente não igual(!==)
 * 
 * DESCRIÇÃO:  Retorna verdadeiro caso os operandos não sejam iguais e/ou não sejam do mesmo tipo.
*/

if(var1 !== "3") {
    console.log("Operador: estritamente não igual. \"3\" !== var1. operandos não são iguais e não são do mesmo tipo.");
}
if(3 !== '3') {
    console.log("Operador: estritamente não igual. 3 !== '3'. operandos não são iguais e não são do mesmo tipo.");
}