/**
 * * O for LOOP PADRÃO
 * 
 *  Vamos começar a explorar alguns exemplos específicos de construções de loop. O primeiro e que você usará na maior parte do tempo, é o loop for - ele tem a seguinte sintaxe:
 * 
 * SINTAXE LOOP FOR:
*/

for(inicializador; condicação-saída; expressão-final) {
    //codigo para executar
}

/**
 * A palavra-chave for, seguido por parênteses.
 * 
 * Dentro do parênteses temos três itens, sepados por ponto e vírgula:
 * 
 * O inicializador— geralmente é uma variável configurada para um número, que é incrementado para contar o número de vezes que o loop foi executado. É também por vezes referido como uma variável de contador.
 * 
 * A condição-saída — como mencionado anteriormente, aqui é definido qundo o loop deve parar de executar. Geralmente, essa é uma expressão que apresenta um operador de comparação, um teste para verificar se a condição de saída foi atendida.
 * 
 * A expressão-final — isso sempre é avaliado (ou executado) cada vez que o loop passou por uma iteração completa. Geralmente serve para incrementar (ou, em alguns casos, decrementar) a variável do contador, aproximando-a do valor da condição de saída.
 * 
 * Algumas chaves contêm um bloco de código - esse código será executado toda vez que o loop for iterado.
*/