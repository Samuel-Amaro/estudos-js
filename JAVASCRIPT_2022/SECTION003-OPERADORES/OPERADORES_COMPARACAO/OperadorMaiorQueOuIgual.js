var var1 = 3;
var var2 = 4;

/**
  * * OPERADOR: Maior que ou igual (>=)
  * 
  * DESCRIÇÃO: Retorna verdadeiro caso o operando da esquerda seja maior ou igual ao da direita.
  * 
  * 
*/

if(var2 >= var1) {
    console.log("Operador: Maior que ou igual (>=). var2 >= var1. operando var2 e maior ou igual a operando var1.");
}
if(var1 >= 3) {
    console.log("Operador: Maior que ou igual (>=). var1 >= 3. operando var1 e maior ou igual a operando 3.");
}